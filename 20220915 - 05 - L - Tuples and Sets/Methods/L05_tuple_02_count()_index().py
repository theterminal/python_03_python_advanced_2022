# ____________ count() _______________ it counts the occurrences of the specified element


t = (10, 10, 23, 34, 45, 23, 10, 45)
print(t.count(10))                          # 3
print(t.count(45))                          # 2


# ____________ index() _______________ returns the index of the specified element


print(t.index(45))


# ____________ len() _________________ used to count the number of the elements in the tuple


print(len(t))
